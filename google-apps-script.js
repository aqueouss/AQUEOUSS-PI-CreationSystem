function doGet(e) {
  return ContentService.createTextOutput(JSON.stringify({ status: "success", message: "AQUEOUSS PI API is active" }))
    .setMimeType(ContentService.MimeType.JSON);
}

function doPost(e) {
  // Handle CORS preflight or direct request
  try {
    const data = JSON.parse(e.postData.contents);
    const action = data.action;
    
    if (action === "login") {
      return handleLogin(data);
    } else if (action === "getDashboard") {
      return handleGetDashboard();
    } else if (action === "generatePI") {
      return handleGeneratePI(data);
    } else {
      return jsonResponse({ status: "error", message: "Invalid action" });
    }
  } catch (error) {
    return jsonResponse({ status: "error", message: error.toString() });
  }
}

function jsonResponse(obj) {
  return ContentService.createTextOutput(JSON.stringify(obj))
    .setMimeType(ContentService.MimeType.JSON);
}

function handleLogin(data) {
  const email = data.email ? data.email.trim().toLowerCase() : "";
  const password = data.password ? data.password.trim() : "";
  
  const ss = SpreadsheetApp.getActiveSpreadsheet();
  const sheet = ss.getSheetByName("Users");
  if (!sheet) {
    return jsonResponse({ status: "error", message: "Users sheet not found" });
  }
  
  const rows = sheet.getDataRange().getValues();
  // Columns: Name | Email | Password | Role
  for (let i = 1; i < rows.length; i++) {
    const rowEmail = rows[i][1] ? rows[i][1].toString().trim().toLowerCase() : "";
    const rowPassword = rows[i][2] ? rows[i][2].toString().trim() : "";
    
    if (rowEmail === email && rowPassword === password) {
      return jsonResponse({
        status: "success",
        user: {
          name: rows[i][0],
          email: rows[i][1],
          role: rows[i][3]
        }
      });
    }
  }
  
  return jsonResponse({ status: "error", message: "Invalid email or password" });
}

function handleGetDashboard() {
  const ss = SpreadsheetApp.getActiveSpreadsheet();
  const logSheet = ss.getSheetByName("PI_Log");
  const settingsSheet = ss.getSheetByName("Settings");
  
  if (!logSheet || !settingsSheet) {
    return jsonResponse({ status: "error", message: "Required sheets (PI_Log or Settings) are missing" });
  }
  
  // Get settings
  const settingsRows = settingsSheet.getDataRange().getValues();
  let prefix = "AQ";
  let fy = "2026-27";
  let lastNum = 0;
  for (let i = 1; i < settingsRows.length; i++) {
    if (settingsRows[i][0] === "Prefix") prefix = settingsRows[i][1];
    if (settingsRows[i][0] === "Financial Year") fy = settingsRows[i][1];
    if (settingsRows[i][0] === "Last PI Number") lastNum = settingsRows[i][1];
  }
  
  const lastPiNumber = `${prefix}/${fy}/${lastNum}`;
  
  // Get logs
  const logRows = logSheet.getDataRange().getValues();
  const recentPIs = [];
  const today = new Date();
  const todayStr = today.toDateString();
  const currentMonth = today.getMonth();
  const currentYear = today.getFullYear();
  
  let todayCount = 0;
  let monthCount = 0;
  
  // Columns in PI_Log: PI Number | Date | Customer Name | Mobile | GSTIN | Taxable Amount | GST Amount | Grand Total | Generated By
  for (let i = logRows.length - 1; i >= 1; i--) {
    const row = logRows[i];
    const piNum = row[0];
    const dateVal = row[1];
    const customer = row[2];
    const mobile = row[3];
    const gstin = row[4];
    const taxable = row[5];
    const gst = row[6];
    const grand = row[7];
    const user = row[8];
    
    let piDate = new Date();
    if (dateVal) {
      piDate = new Date(dateVal);
    }
    
    // Check if generated today
    if (piDate.toDateString() === todayStr) {
      todayCount++;
    }
    // Check if generated this month
    if (piDate.getMonth() === currentMonth && piDate.getFullYear() === currentYear) {
      monthCount++;
    }
    
    if (recentPIs.length < 10 && piNum) {
      recentPIs.push({
        piNumber: piNum,
        date: dateVal,
        customerName: customer,
        mobile: mobile,
        gstin: gstin,
        taxableAmount: taxable,
        gstAmount: gst,
        grandTotal: grand,
        generatedBy: user
      });
    }
  }
  
  return jsonResponse({
    status: "success",
    stats: {
      todayCount,
      monthCount,
      lastPiNumber
    },
    recentPIs
  });
}

function handleGeneratePI(data) {
  // Use LockService to prevent race conditions during number generation
  const lock = LockService.getScriptLock();
  try {
    // Wait up to 15 seconds for lock
    if (!lock.tryLock(15000)) {
      return jsonResponse({ status: "error", message: "Could not acquire sheet lock. Please try again." });
    }
    
    const ss = SpreadsheetApp.getActiveSpreadsheet();
    const settingsSheet = ss.getSheetByName("Settings");
    const logSheet = ss.getSheetByName("PI_Log");
    
    if (!settingsSheet || !logSheet) {
      return jsonResponse({ status: "error", message: "Required sheets (Settings or PI_Log) missing" });
    }
    
    // Read Settings
    const settingsRows = settingsSheet.getDataRange().getValues();
    let prefix = "AQ";
    let fy = "2026-27";
    let lastNumIdx = -1;
    let lastNum = 0;
    
    for (let i = 1; i < settingsRows.length; i++) {
      if (settingsRows[i][0] === "Prefix") prefix = settingsRows[i][1];
      if (settingsRows[i][0] === "Financial Year") fy = settingsRows[i][1];
      if (settingsRows[i][0] === "Last PI Number") {
        lastNumIdx = i + 1; // 1-indexed row number
        lastNum = parseInt(settingsRows[i][1], 10) || 0;
      }
    }
    
    if (lastNumIdx === -1) {
      return jsonResponse({ status: "error", message: "Last PI Number setting row not found in Settings sheet" });
    }
    
    // Increment PI Number
    const newNum = lastNum + 1;
    const generatedPiNumber = `${prefix}/${fy}/${newNum}`;
    
    // Write new number to settings sheet
    settingsSheet.getRange(lastNumIdx, 2).setValue(newNum);
    
    // Append to PI_Log
    // Columns: PI Number | Date | Customer Name | Mobile | GSTIN | Taxable Amount | GST Amount | Grand Total | Generated By
    logSheet.appendRow([
      generatedPiNumber,
      data.date || new Date().toISOString().split('T')[0],
      data.customerName || "",
      data.mobile || "",
      data.gstin || "",
      data.taxableAmount || 0,
      data.gstAmount || 0,
      data.grandTotal || 0,
      data.generatedBy || "System"
    ]);
    
    // Force write database execution before releasing lock
    SpreadsheetApp.flush();
    
    return jsonResponse({
      status: "success",
      piNumber: generatedPiNumber
    });
    
  } catch (error) {
    return jsonResponse({ status: "error", message: error.toString() });
  } finally {
    // Release the lock
    lock.releaseLock();
  }
}

